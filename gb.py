@st.cache_data
def load_data(path):
    df = pd.read_excel(path, engine="openpyxl")
    # Create lead-time as (Ship Date - Order Date) in days
    df['Lead Time'] = (df['Ship Date'] - df['Order Date']).dt.days
    return df

